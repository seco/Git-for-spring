package co.kr.auction.model;

public class CategoryDto {
	private int category_bun, category_subbun, category_count;
	private String category_name;
	
	public int getCategory_bun() {
		return category_bun;
	}
	public void setCategory_bun(int category_bun) {
		this.category_bun = category_bun;
	}
	public int getCategory_subbun() {
		return category_subbun;
	}
	public void setCategory_subbun(int category_subbun) {
		this.category_subbun = category_subbun;
	}
	public int getCategory_count() {
		return category_count;
	}
	public void setCategory_count(int category_count) {
		this.category_count = category_count;
	}
	public String getCategory_name() {
		return category_name;
	}
	public void setCategory_name(String category_name) {
		this.category_name = category_name;
	}
	 
	 
}
