public class BoardDto {
	private int b_no, b_pbun, b_gnum, b_onum;
	private String b_memid, b_date,	b_content;
	
	public int getB_no() {
		return b_no;
	}
	
	public void setB_no(int b_no) {
		this.b_no = b_no;
	}
	
	public int getB_pbun() {
		return b_pbun;
	}
	
	public void setB_pbun(int b_pbun) {
		this.b_pbun = b_pbun;
	}
	
	public int getB_gnum() {
		return b_gnum;
	}
	
	public void setB_gnum(int b_gnum) {
		this.b_gnum = b_gnum;
	}
	
	public int getB_onum() {
		return b_onum;
	}
	
	public void setB_onum(int b_onum) {
		this.b_onum = b_onum;
	}
	
	public String getB_memid() {
		return b_memid;
	}
	
	public void setB_memid(String b_memid) {
		this.b_memid = b_memid;
	}
	
	public String getB_date() {
		return b_date;
	}
	
	public void setB_date(String b_date) {
		this.b_date = b_date;
	}
	
	public String getB_content() {
		return b_content;
	}
	
	public void setB_content(String b_content) {
		this.b_content = b_content;
	}
	
	
}
