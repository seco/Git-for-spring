package pack.model;

public class JikwonDto {
	private String jikwon_no, jikwon_id, jikwon_name, jikwon_password;

	public String getJikwon_no() {
		return jikwon_no;
	}

	public void setJikwon_no(String jikwon_no) {
		this.jikwon_no = jikwon_no;
	}

	public String getJikwon_id() {
		return jikwon_id;
	}

	public void setJikwon_id(String jikwon_id) {
		this.jikwon_id = jikwon_id;
	}

	public String getJikwon_name() {
		return jikwon_name;
	}

	public void setJikwon_name(String jikwon_name) {
		this.jikwon_name = jikwon_name;
	}

	public String getJikwon_password() {
		return jikwon_password;
	}

	public void setJikwon_password(String jikwon_password) {
		this.jikwon_password = jikwon_password;
	}
	
	
}
