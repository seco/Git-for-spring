package pack.model;

public class SangOrderDto {
	private String gogek_id, register_date, state, sang_no, sang_order_cnt, sang_Pname;
	private int sang_order_no;
	
	public int getSang_order_no() {
		return sang_order_no;
	}

	public void setSang_order_no(int sang_order_no) {
		this.sang_order_no = sang_order_no;
	}

	public String getGogek_id() {
		return gogek_id;
	}

	public void setGogek_id(String gogek_id) {
		this.gogek_id = gogek_id;
	}

	public String getRegister_date() {
		return register_date;
	}

	public void setRegister_date(String register_date) {
		this.register_date = register_date;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getSang_no() {
		return sang_no;
	}

	public void setSang_no(String sang_no) {
		this.sang_no = sang_no;
	}

	public String getSang_order_cnt() {
		return sang_order_cnt;
	}

	public void setSang_order_cnt(String sang_order_cnt) {
		this.sang_order_cnt = sang_order_cnt;
	}

	public String getSang_Pname() {
		return sang_Pname;
	}

	public void setSang_Pname(String sang_Pname) {
		this.sang_Pname = sang_Pname;
	}
	
	
}
