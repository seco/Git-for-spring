package pack.model;

public class ProgramDto {
	String pro_no, pro_name, pro_su, pro_price, pro_detail, pro_image, pro_cate;

	public String getPro_no() {
		return pro_no;
	}

	public void setPro_no(String pro_no) {
		this.pro_no = pro_no;
	}

	public String getPro_name() {
		return pro_name;
	}

	public void setPro_name(String pro_name) {
		this.pro_name = pro_name;
	}

	public String getPro_su() {
		return pro_su;
	}

	public void setPro_su(String pro_su) {
		this.pro_su = pro_su;
	}

	public String getPro_price() {
		return pro_price;
	}

	public void setPro_price(String pro_price) {
		this.pro_price = pro_price;
	}

	public String getPro_detail() {
		return pro_detail;
	}

	public void setPro_detail(String pro_detail) {
		this.pro_detail = pro_detail;
	}

	public String getPro_image() {
		return pro_image;
	}

	public void setPro_image(String pro_image) {
		this.pro_image = pro_image;
	}

	public String getPro_cate() {
		return pro_cate;
	}

	public void setPro_cate(String pro_cate) {
		this.pro_cate = pro_cate;
	}
	
	
	

}
